package model;

public class StringClass {
    private String str;
    private String Result;

    public StringClass(String str, String Result) {
        this.str = str;
        this.Result = Result;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }

    public String getResult() {
        return Result;
    }

    public void setResult(String Result) {
        this.Result = Result;
    }


    
    
    
}
